package ex6;

public class Vagoes {
    private String numeroDeSerie;
    private String tipo;
    private Double capacidadeDeCarga;
    private Double comprimentoDeTesteiras;
    private Double comprimentoDeEngates;


    public String getNumeroDeSerie() {
        return numeroDeSerie;
    }

    public String getTipo() {
        return tipo;
    }

    public Double getCapacidadeDeCarga() {
        return capacidadeDeCarga;
    }

    public Double getComprimentoDeTesteiras() {
        return comprimentoDeTesteiras;
    }

    public Double getComprimentoDeEngates() {
        return comprimentoDeEngates;
    }
}
